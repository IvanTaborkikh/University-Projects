package org.padadak.backEnd.data;

public class Plyta {
    int nr;
    float promien;

    public Plyta(int nr, float promien) {
        this.nr = nr;
        this.promien = promien;

    }

    public int getNr() {
        return nr;
    }

    public float getPromien() {
        return promien;
    }

}
