package org.padadak.client.objects;

public class ShipsInPorts {
    private String date;
    private int gdansk;
    private int szczecin;
    private int gdynia;
    private int id;
    private int swinoujscie;

    public ShipsInPorts() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public int getGdansk() {
        return gdansk;
    }

    public void setGdansk(int gdansk) {
        this.gdansk = gdansk;
    }

    public int getSzczecin() {
        return szczecin;
    }

    public void setSzczecin(int szczecin) {
        this.szczecin = szczecin;
    }

    public int getGdynia() {
        return gdynia;
    }

    public void setGdynia(int gdynia) {
        this.gdynia = gdynia;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getSwinoujscie() {
        return swinoujscie;
    }

    public void setSwinoujscie(int swinoujscie) {
        this.swinoujscie = swinoujscie;
    }

}
